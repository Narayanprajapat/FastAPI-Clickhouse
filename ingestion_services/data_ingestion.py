class FyersWebSocketClient:

    def __init__(
        self,
        client_id: str,
        access_token: str,
        all_symbols: list,
        batch_size: int,
        interval: int,
    ):
        self.final_token = f"{client_id}:{access_token}"
        self.all_symbols = all_symbols
        self.batch_size = batch_size
        self.interval = interval
        self.current_batch_index = 0
        self.fyers = data_ws.FyersDataSocket(
            access_token=self.final_token,
            log_path="",
            litemode=False,
            write_to_file=False,
            reconnect=True,
            on_connect=self.on_open,
            on_close=self.on_close,
            on_error=self.on_error,
            on_message=self.on_message,
        )

    def inserting_into_clickhouse(self, values: list) -> None:
        try:
            with clickhouse_pool.pool.get_client() as client:
                query = """
                    INSERT INTO 
                        market.ticks (
                            event_time, 
                            date, 
                            symbol, 
                            open, 
                            high, 
                            low, 
                            close, 
                            volume, 
                            prev_close_price
                        ) 
                    VALUES
                """
                client.execute(query, values)

            # print("data inserted sucessfully into clickhouse")
        except Exception as e:
            print("Exception occurred--", e)

    def data_processing(self, raw_data: dict) -> list:
        ts = raw_data["last_traded_time"]
        dt_local = datetime.datetime.fromtimestamp(ts)
        info = {
            "symbol": raw_data["symbol"].replace("NSE:", "").replace("-EQ", ""),
            "date": dt_local.date(),
            "event_time": dt_local,
            "open": raw_data["open_price"],
            "close": raw_data["ltp"],
            "low": raw_data["low_price"],
            "high": raw_data["high_price"],
            "volume": raw_data["vol_traded_today"],
            "prev_close_price": raw_data["prev_close_price"],
        }

        stock_info = Stock(**info).model_dump()

        values = [
            (
                stock_info["event_time"],
                stock_info["date"],
                stock_info["symbol"],
                stock_info["open"],
                stock_info["high"],
                stock_info["low"],
                stock_info["close"],
                stock_info["volume"],
                stock_info["prev_close_price"],
            )
        ]
        return values

    def on_message(self, message):
        # print("Response:", message)
        try:
            if "type" in message and message["type"] == "sf":
                values = self.data_processing(raw_data=message)
                self.inserting_into_clickhouse(values=values)
        except Exception as e:
            print("Exception", e)

    def on_error(self, message):
        print("Error:", message)

    def on_close(self, message):
        print("Connection closed:", message)

    def on_open(self):
        print("âœ… WebSocket Connected. Starting rotation thread...")
        threading.Thread(target=self.rotate_batches, daemon=True).start()

        # print("WebSocket Connected. Subscribing to symbols...", self.symbols)
        # data_type = "SymbolUpdate"

        # self.fyers.subscribe(symbols=self.symbols, data_type=data_type)
        # self.fyers.keep_running()

    def rotate_batches(self):
        """Rotate through symbol batches dynamically."""
        batches = [
            self.all_symbols[i : i + self.batch_size]
            for i in range(0, len(self.all_symbols), self.batch_size)
        ]

        while True:
            current_batch = batches[self.current_batch_index]
            print(
                f"ðŸ”„ Subscribing batch {self.current_batch_index+1}/{len(batches)} "
                f"with {len(current_batch)} symbols"
            )

            if self.current_batch_index > 0:
                prev_batch = batches[self.current_batch_index - 1]
                print(
                    f"ðŸšª Unsubscribing batch {self.current_batch_index}/{len(batches)}"
                )
                self.fyers.unsubscribe(symbols=prev_batch, data_type="SymbolUpdate")

            # Subscribe to new batch
            self.fyers.subscribe(symbols=current_batch, data_type="SymbolUpdate")

            # Wait before switching
            time.sleep(self.interval)

            # Move to next batch
            self.current_batch_index = (self.current_batch_index + 1) % len(batches)

    def connect(self):
        print("Connecting to Fyers WebSocket...")
        self.fyers.connect()


